package com.juaracoding.dto.report;

import java.util.Date;

public class MenuReportDTO {
    private Long idMenu;
    private String namaMenu;
    private String pathMenu;
    private String endPoint;
    private String menuHeaderName;
    private Date createdDate;
    private Date modifiedDate;
    private Byte isDelete;

    // Getters and Setters

    public Long getIdMenu() {
        return idMenu;
    }

    public void setIdMenu(Long idMenu) {
        this.idMenu = idMenu;
    }

    public String getNamaMenu() {
        return namaMenu;
    }

    public void setNamaMenu(String namaMenu) {
        this.namaMenu = namaMenu;
    }

    public String getPathMenu() {
        return pathMenu;
    }

    public void setPathMenu(String pathMenu) {
        this.pathMenu = pathMenu;
    }

    public String getEndPoint() {
        return endPoint;
    }

    public void setEndPoint(String endPoint) {
        this.endPoint = endPoint;
    }

    public String getMenuHeaderName() {
        return menuHeaderName;
    }

    public void setMenuHeaderName(String menuHeaderName) {
        this.menuHeaderName = menuHeaderName;
    }

    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public Date getModifiedDate() {
        return modifiedDate;
    }

    public void setModifiedDate(Date modifiedDate) {
        this.modifiedDate = modifiedDate;
    }

    public Byte getIsDelete() {
        return isDelete;
    }

    public void setIsDelete(Byte isDelete) {
        this.isDelete = isDelete;
    }
}
